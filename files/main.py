# 1. Definir la lista de números
numeros = [1, 2, 3, 4, 5]

# 2. Inicializar una variable para almacenar la suma
suma_total = 0

# 3. Recorrer la lista con un bucle for y sumar cada elemento
for numero in numeros:
    suma_total = suma_total + numero
    # También se puede escribir como: suma_total += numero

# 4. Imprimir el resultado
print(f"La lista es: {numeros}")
print(f"La suma de los valores es: {suma_total}")